#include "lists.h"

/**
 * find_listint_loop - a function that finds the loop in a linked list
 * @head: The head of a passed list
 * Return: NULL if list is not a loop or the address where the loop starts
 */
listint_t *find_listint_loop(listint_t *head)
{
	listint_t *slow = head;
	listint_t *fast = head;

	while (fast && fast->next)
	{
		slow = slow->next;
		fast = fast->next->next;

		if (slow == fast)
		{
			slow = head;
			while (slow != fast)
			{
				slow = slow->next;
				fast = fast->next;
			}
			return (slow);
		}
	}
	return (NULL);
}
